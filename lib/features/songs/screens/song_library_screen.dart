class SectionBar {
  final String chord;
  final int beats;
  const SectionBar(this.chord, [this.beats = 4]);
}

class SongSection {
  final String label;
  final List<SectionBar> bars;
  final int repeats;
  const SongSection({required this.label, required this.bars, this.repeats = 1});
}

class SongData {
  final String id;
  final String title;
  final String artist;
  final int difficulty;
  final String genre;
  final String lessonRef;
  final int bpm;
  final String tuning;
  final List<SongSection> sections;
  final int capo;
  final String key;
  final String strumPattern;
  final String? requiredModuleId; // null = immer frei

  const SongData({
    required this.id,
    required this.title,
    required this.artist,
    required this.difficulty,
    required this.genre,
    required this.sections,
    required this.lessonRef,
    required this.bpm,
    this.tuning = 'Standard',
    this.capo = 0,
    this.key = '',
    this.strumPattern = 'D·D·D·D',
    this.requiredModuleId,
  });

  List<String> get chords {
    final seen = <String>{};
    return sections
        .expand((s) => s.bars.map((b) => b.chord))
        .where(seen.add)
        .toList();
  }
}

